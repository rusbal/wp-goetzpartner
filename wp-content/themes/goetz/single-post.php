                                            <div
                                                class="wpb_wrapper">
                                                <p>Am vergangenen Freitag wurde unser Partner GartenLandschaft Berg als „Gartendesigner des Jahres“ mit dem <a
                                                        href="http://taspoawards.de/gewinner/" target="_blank">TASPO Award</a> geehrt – Herzlichen Glückwunsch!</p>
                                                <p>Der &#8222;Oscar der grünen Branche&#8220; wird seit 2006 von der Wochenzeitung TASPO in verschiedenen Kategorien verliehen. <a
                                                        href="http://gartenlandschaft.com/de/GartenLandschaft-Berg/Aktuelles" target="_blank">GartenLandschaft Berg</a> wurde bereits 2011 und 2014 als „<a
                                                        href="http://taspoawards.de/gewinner/" target="_blank">Gartendesigner des Jahres</a>“ ausgezeichnet. In diesem Jahr konnte er die Fachjury mit der <a
                                                        href="https://www.fachwerk4.de/projekt/haus-eifel/" target="_blank">Gartengestaltung unseres Projektes Haus Eifel</a> überzeugen.</p>
                                                <p>In ihrer Begründung hebt die Jury besonders die einzigartige Verbindung zwischen moderner Architektur und weiter landschaftlicher Fläche ohne optische Einschränkungen hervor. „Im Fokus der Planung standen der Umgang mit dem Blick in die Weiten und Wälder der Eifel.“ Auch die beiden Terrassensituationen wurden gewürdigt: „eine im Erdgeschoss mit formalem Pool entlang der Hausseite und Endlosblick in die weite Eifellandschaft. Und eine auf der Balkon-Terrasse im ersten Obergeschoss mit einem formalen Wasserbecken mit Lounge-Bereich. In beiden Fällen fungiert das Wasser als direkte Verbindung zur Natur. Die Einzigartigkeit liegt in der mutigen Reduktion. Die Innovation ist die 4,60 mal 2,60 Meter große und einen halben Meter tiefe Wasserfläche auf dem großen Balkon, in der sich der Himmel spiegelt.“</p>
